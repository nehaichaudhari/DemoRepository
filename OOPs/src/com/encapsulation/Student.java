package com.encapsulation;

public class Student {
	private int sid;
	private String sname;
	private float marks;
	
	public void setSid(int sid) {
		this.sid = sid;
	}
	
	public int getSid() {
		return sid;
	}
	
	public void setSname(String sname) {
		this.sname = sname;
	}
	
	public String getSname() {
		return sname;
	}
	
	public void setMarks(float marks) {
		this.marks = marks;
	}
	
	public float getMarks() {
		return marks;
	}

}
